<template>
    <div class="login-container card">
        <h2>Web Login</h2>
        <form @submit.prevent="handleLogin">
            <div class="form-group">
                <label for="telegramId">Telegram ID:</label>
                <input type="number" id="telegramId" v-model="tgId" required>
            </div>
            <div class="form-group">
                <label for="password">Password:</label>
                <input type="password" id="password" v-model="password" required>
            </div>
            <button type="submit" :disabled="isLoading">Login</button>
            <div v-if="error" class="error-message">{{ error }}</div>
             <p class="hint">Set your password using the /setpassword command in the Telegram bot.</p>
        </form>
    </div>
</template>

<script setup>
import { ref, defineEmits } from 'vue';
import apiService from '@/utils/api.js';

const emit = defineEmits(['login-success']);
const tgId = ref('');
const password = ref('');
const isLoading = ref(false);
const error = ref(null);

const handleLogin = async () => {
    isLoading.value = true;
    error.value = null;

    // Basic validation
    if (!tgId.value || !password.value) {
        error.value = 'Please enter both Telegram ID and password.';
        isLoading.value = false;
        return;
    }

    try {
        // Use site-local proxy to keep cookies first-party
        const response = await fetch('/api/web-login', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
            },
            credentials: 'include', // Important for cookies
            body: JSON.stringify({
                tg_id: parseInt(tgId.value, 10), // Ensure it's a number
                password: password.value,
            }),
        });

        if (!response.ok) {
            const errorData = await response.json();
            throw new Error(errorData.error || 'Login failed.');
        }

        const data = await response.json();
        if (!data.success) throw new Error('Login failed.');
        if (data.accessToken) {
          apiService.setTokens({ accessToken: data.accessToken, refreshToken: data.refreshToken });
        }
        console.log('Login successful. Tokens received and stored.');

        // Emit an event to indicate successful login
        emit('login-success');

    } catch (err) {
        console.error('Login error:', err);
        error.value = err.message;
    } finally {
        isLoading.value = false;
    }
};
</script>

